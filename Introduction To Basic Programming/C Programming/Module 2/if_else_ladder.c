#include <stdio.h>
int main()
{
    int tk;
    scanf("%d", &tk);
    if (tk >= 100)
    {
        printf("Burger khabo\n");
    }
    else if (tk >= 50)
    {
        printf("Fuchka Khabo\n");
    }
    else if (tk >= 30)
    {
        printf("Mojo & chips khabo");
    }
    else if (tk >= 20)
    {
        printf("Mojo Khabo");
    }
    else if (tk = 10)
    {
        printf("Chips Khabo");
    }
    else
    {
        printf("Kichui khabo na\n");
    }
    return 0;
}