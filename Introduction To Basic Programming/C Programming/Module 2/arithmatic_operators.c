#include <stdio.h>
int main()
{
    int a = 10;
    float b = 8;
    int sum = a + b;
    printf("Summation = %d\n", sum);
    int sub = a - b;
    printf("Subtraction = %d\n", sub);
    int mul = a * b;
    printf("Multiplication =%d\n", mul);
    float div = a / b;
    printf("Division = %f\n", div);
    return 0;
}
